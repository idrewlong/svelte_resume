<script>
        export let showOnPx = 150;
        let hidden = true;
      
        function goTop() {
          document.body.scrollIntoView();
        }
      
        function scrollContainer() {
          return document.documentElement || document.body;
        }
      
        function handleOnScroll() {
          if (!scrollContainer()) {
            return;
          }
      
          if (scrollContainer().scrollTop > showOnPx) {
            hidden = false;
          } else {
            hidden = true;
          }
        }
      </script>
      
      <style>
        .back-to-top {
          opacity: 1;
          transition: opacity 0.5s, visibility 0.5s;
          position: fixed;
          z-index: 99;
          right: 20px;
          user-select: none;
          bottom: 20px;
          color: white;
          
        }
      
        .back-to-top.hidden {
          opacity: 0;
          visibility: hidden;
        }
      </style>
      
      <svelte:window on:scroll={handleOnScroll} />
      
      <button class="back-to-top bg-gradient-to-br from-red-600 to-zinc-600 px-4 py-2 rounded-md" on:click={goTop} class:hidden>Back to top</button>